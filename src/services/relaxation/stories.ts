/**
 * 获取所有故事
 */
export function getAllStories(): BedtimeStory[] {
  return BEDTIME_STORIES;
}

/**
 * 根据ID获取故事
 */
export function getStoryById(id: string): BedtimeStory | undefined {
  return BEDTIME_STORIES.find((s) => s.id === id);
}

/**
 * 根据类别筛选故事
 */
export function getStoriesByCategory(category: BedtimeStory['category']): BedtimeStory[] {
  return BEDTIME_STORIES.filter((s) => s.category === category);
}

/**
 * 随机获取一个故事
 */
export function getRandomStory(): BedtimeStory {
  const index = Math.floor(Math.random() * BEDTIME_STORIES.length);
  return BEDTIME_STORIES[index]!;
}
